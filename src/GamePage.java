    public void inc_game_count(){
        
        String query="Select * from gamers where Username = ?";
        
        try {
            db.psqlquery=db.con.prepareStatement(query);
            db.psqlquery.setString(1,gamer.getUsername());
            ResultSet res=db.psqlquery.executeQuery();
            
            int game_count=0;
            
            while(res.next()){
                game_count=res.getInt("game_count");
            }
            
        query="Update gamers set game_count = ? where Username = ?";
            
            db.psqlquery=db.con.prepareStatement(query);
            db.psqlquery.setInt(1,game_count+1);
            db.psqlquery.setString(2,gamer.getUsername());
            db.psqlquery.executeUpdate();
        
            
            
        } catch (SQLException ex) {
            Logger.getLogger(GamePage.class.getName()).log(Level.SEVERE, null, ex);
        }
  
    }
