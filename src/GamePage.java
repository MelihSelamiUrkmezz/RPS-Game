    private void rockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rockActionPerformed
        inc_game_count();
        String value=RandomVariable();
        String score;
        score=this.score2.getText();
        if(value.equals("Paper")){
            this.value.setText("Paper");
            String[] arr=score.split("-");
            int pc=Integer.parseInt(arr[1]);
            int user=Integer.parseInt(arr[0]);
            pc++;
            score2.setText(user+"-"+pc);
        }
        else if(value.equals("Scissors")){
            inc_win_count();
              this.value.setText("Scissors");
              String[] arr=score.split("-");
            int pc=Integer.parseInt(arr[1]);
            int user=Integer.parseInt(arr[0]);
            user++;
            score2.setText(user+"-"+pc);
        }
        else{
            this.value.setText("Rock");
        }
   
    }//GEN-LAST:event_rockActionPerformed

